function y = polynomial(x, coefficients)
    k = length(coefficients);
    y = 0;
    for n=0:k-1
        y = y + (x.^n).*coefficients(k-n);
    end
end


function loss = MSE(y, y_hat)
    loss = (y-y_hat).^2;
    loss = mean(loss);
end